def generate_recommendations(features):
    recs = []

    if features['bmi'] > 25:
        recs.append("🥗 Рекомендуется скорректировать питание и увеличить ежедневную физическую активность.")

    if features['sys'] > 130 or features['dia'] > 85:
        recs.append("❤️ Контроль артериального давления и консультация специалиста могут снизить риск осложнений.")

    if features['sleep_def'] > 1:
        recs.append("😴 Увеличение продолжительности сна до 7–8 часов способствует восстановлению организма.")

    if features['activity'] > 0:
        recs.append("🚶 Регулярные прогулки или умеренные тренировки 3–4 раза в неделю улучшат общее состояние.")

    if not recs:
        recs.append("✅ Рекомендуется поддерживать текущий образ жизни.")

    return recs